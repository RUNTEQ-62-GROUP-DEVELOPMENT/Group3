# StudyRecordsController
# 学習記録の作成、確認、保存を担当するコントローラー
# - new: 今日の学習記録を入力するためのフォームを表示
# - create: 入力データを一時保存（データベースにはまだ保存しない）
# - confirm: 入力内容を確認するページを表示
# - commit: 学習記録を最終保存
class StudyRecordsController < ApplicationController
  # 今日の学習記録作成の新規作成
  def new
  end

  # 今日の学習記録の新規保存
  def create
  end

  # 今日の学習記録の確認
  def confirm
  end

  # 今日の学習記録の保存
  def commit
  end
end
